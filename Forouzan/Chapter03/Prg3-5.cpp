/************************************************************
 * A program which checks addition, subtraction expressions *
************************************************************/
#include <iostream>
using namespace std;

int main() {
    // addition operator
    cout << "Check the addition operator" << endl;
    cout << "30 + 5 = " << 30 + 5 << endl;
    cout << "20.5 + 6.2 = " << 20.5 + 6.2 << endl;
    // subtraction operator
    cout << "Check the subtraction operator" << endl;
    cout << "5 - 30 = " << 5 - 30 << endl;
    cout << "51.2 - 30.4 = " << 51.2 - 30.4 << endl;
    return 0;
}