/*****************************************************************
 * A program which shows a few plus/minus expressions  *
*****************************************************************/
#include <iostream>
using namespace std;

int main() {
    // declaration and initialization of variables
    int x = 4;
    int y = -10;
    // application plus/minus operators to variable x
    cout << "Apply plus operator to x : " << +x << endl;
    cout << "Apply minus operator to x : " << -x << endl;
    // applicaiton plus/minus operators to variable y
    cout << "Apply plus operator to y : " << +y << endl;
    cout << "Apply minus operator to y : " << -y << endl;
    return 0;
}