/******************************************************
 * A program which checks multiplication expressions  *
******************************************************/
#include <iostream>
using namespace std;

int main() {
    // multiplication
    cout << "Check the multiplication operator" << endl;
    cout << "3 * 4 = " << 3 * 4 << endl;
    cout << "2.4 * 4.1 = " << 2.4 * 4.1 << endl;
    cout << "-3 * 4 = " << -3 * 4 << endl;
    // division
    cout << "Check the division operator" << endl;
    cout << "30 / 5 = " << 30 / 5 << endl;
    cout << "4 / 7 = " << 4 / 7 << endl;
    // modulo
    cout << "Check the modulo operator" << endl;
    cout << "30 % 5 = " << 30 % 5 << endl;
    cout << "30 % 4 = " << 30 % 4 << endl;
    cout << "3 % 7 = " << 3 % 7 << endl;
    return 0;
}