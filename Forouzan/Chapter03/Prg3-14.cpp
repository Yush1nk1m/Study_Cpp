/*******************************************************************
 * Evaluation of a simple expression which represents parenthesis  *
*******************************************************************/
#include <iostream>
using namespace std;

int main() {
    // declaration
    int x = 5;
    // print the value of the expression
    cout << "The value of '(x + 6) * 7' : " << (x + 6) * 7;
    return 0;
}