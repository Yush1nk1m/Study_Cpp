/****************************************************************
 * A program which uses literal when it initializes variables   *
****************************************************************/
#include <iostream>
using namespace std;

int main() {
    // declaration and initialization of variables
    int x = -1245;
    unsigned int y = 1245;
    unsigned int z = -2367;
    unsigned int t = 14.56;
    // print initialized values
    cout << x << endl;
    cout << y << endl;
    cout << z << endl;
    cout << t;
    return 0;
}