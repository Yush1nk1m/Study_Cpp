/***********************************************************
 * A program which checks size of three types of integer   *
***********************************************************/
#include <iostream>
using namespace std;

int main() {
    cout << "The size of short int type is " << sizeof(short int) << " bytes." << endl;
    cout << "The size of int type is " << sizeof(int) << " bytes." << endl;
    cout << "The size of long int is " << sizeof(long int) << " bytes." << endl;
    return 0;
}