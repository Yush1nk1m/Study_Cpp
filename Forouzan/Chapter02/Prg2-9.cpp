/****************************************************************
 * A program which declares and initializes char type variable  *
****************************************************************/
#include <iostream>
using namespace std;

int main() {
    // declaration and intialization of char type variables
    char first = 'A';
    char second = 65;
    char third = 'B';
    char fourth = 66;
    // print value
    cout << "The value of first : " << first << endl;
    cout << "The value of second : " << second << endl;
    cout << "The value of third : " << third << endl;
    cout << "The value of fourth : " << fourth << endl;
    return 0;
}