/**************************************************************
 * A program which uses bool type variables and their values  *
**************************************************************/
#include <iostream>
using namespace std;

int main() {
    // declaration of variables
    bool x = 123;
    bool y = -8;
    bool z = 0;
    bool t = -0;
    bool u = true;
    bool v = false;
    // print values
    cout << "The value of x : " << x << endl;
    cout << "The value of y : " << y << endl;
    cout << "The value of z : " << z << endl;
    cout << "The value of t : " << t << endl;
    cout << "The value of u : " << u << endl;
    cout << "The value of v : " << v << endl;
    return 0;
}