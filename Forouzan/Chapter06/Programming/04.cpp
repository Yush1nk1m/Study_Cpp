#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    cout << "round(23.2) = " << round(23.2) << endl;
    cout << "round(23.8) = " << round(23.8) << endl;
    cout << "round(-23.2) = " << round(-23.2) << endl;
    cout << "round(-23.8) = " << round(-23.8) << endl;
    return 0;
}