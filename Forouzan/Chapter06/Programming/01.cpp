#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    cout << "abs(25) = " << abs(25) << endl;
    cout << "abs(-23) = " << abs(-23) << endl;
    cout << "floor(44.56) = " << floor(44.56) << endl;
    cout << "floor(-23.78) = " << floor(-23.78) << endl;
    cout << "ceil(25.23) = " << ceil(25.23) << endl;
    cout << "ceil(-2.89) = " << ceil(-2.89) << endl;
    return 0;
}